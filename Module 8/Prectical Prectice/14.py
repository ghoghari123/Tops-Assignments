# Write a Python program to access values using keys from a dictionary.

Stud_Info = {
    "Name" : "Hemanshi",
    "Age" : 22,
    "Course" : "Full Stack Development",
    "City" : "Surat"
}

print(f"Name : {Stud_Info['Name']}")
print(f"Age : {Stud_Info['Age']}")
print(f"Course : {Stud_Info['Course']}")
print(f"City : {Stud_Info['City']}")