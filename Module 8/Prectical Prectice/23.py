# Write a Python program to demonstrate the use of functions from the math module.

import math

print("Demostrate The Use Of Inbuilt Function From The Math Module")
print(f"Factorial Value : {math.factorial(8)}")
print(f"Pie Value : {math.pi}")
print(f"Sin Value : {math.sin(2)}")
print(f"Cost Value : {math.cos(4)}")
print(f"Square Root : {math.sqrt(6)}")
print(f"Floor Value : {math.floor(8.03)}")